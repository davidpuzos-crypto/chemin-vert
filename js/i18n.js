/* -------------------- Moteur i18n -------------------- */
const I18nEngine = {
  current: "fr",
  KEY: "cv_lang",

  detect() {
    const saved = localStorage.getItem(this.KEY);
    if (saved && I18N.languages[saved]) return saved;
    const nav = (navigator.language || "fr").slice(0, 2).toLowerCase();
    return I18N.languages[nav] ? nav : "fr";
  },

  apply(lang) {
    if (!I18N.languages[lang]) lang = "fr";
    this.current = lang;
    localStorage.setItem(this.KEY, lang);

    const meta = I18N.languages[lang];
    const html = document.documentElement;
    html.setAttribute("lang", lang);
    html.setAttribute("dir", meta.dir);
    document.body.classList.toggle("rtl", meta.dir === "rtl");

    const dict = I18N.t[lang];

    // Texte des éléments [data-i18n]
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      if (dict[key] !== undefined) el.textContent = dict[key];
    });
    // Attributs [data-i18n-attr="attr:key,attr2:key2"]
    document.querySelectorAll("[data-i18n-attr]").forEach(el => {
      el.getAttribute("data-i18n-attr").split(",").forEach(pair => {
        const [attr, key] = pair.split(":");
        if (dict[key] !== undefined) el.setAttribute(attr, dict[key]);
      });
    });
    // <title> et meta description
    if (dict["meta.title"]) document.title = dict["meta.title"];
    const md = document.querySelector('meta[name="description"]');
    if (md && dict["meta.desc"]) md.setAttribute("content", dict["meta.desc"]);

    // Notifie les autres modules (valeurs de la charte, sélecteur…)
    document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
  },

  get(key) {
    return (I18N.t[this.current] && I18N.t[this.current][key]) || key;
  }
};
